package core;

public class ArithmeticSolver {

	
	public double add(double leftOpp, double rightOpp) {
		return leftOpp + rightOpp;
	}
	
	public double subtract(double leftOpp, double rightOpp) {
		return leftOpp - rightOpp;
	}
	
	public double multiply(double leftOpp, double rightOpp) {
		return leftOpp * rightOpp;
	}
	
	public double divide(double leftOpp, double rightOpp) {
		return leftOpp / rightOpp;
	}
	
	public double modulus(double leftOpp, double rightOpp) {
		return leftOpp % rightOpp;
	}
}
